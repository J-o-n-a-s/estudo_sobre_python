from math import cos, radians, sin, tan
angulo = float(input('Digite o 창ngulo que deseja: '))
print(f'O 창ngulo de {angulo:.2f} tem o cosseno {cos(radians(angulo)):.2f}.')
print(f'O 창ngulo de {angulo:.2f} tem o seno {sin(radians(angulo)):.2f}.')
print(f'O 창ngulo de {angulo:.2f} tem a tangente {tan(radians(angulo)):.2f}.')