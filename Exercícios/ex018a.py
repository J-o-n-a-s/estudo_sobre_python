import math
angulo = float(input('Digite o 창ngulo que deseja: '))
print(f'O 창ngulo de {angulo:.2f} tem o cosseno {math.cos(math.radians(angulo)):.2f}.')
print(f'O 창ngulo de {angulo:.2f} tem o seno {math.sin(math.radians(angulo)):.2f}.')
print(f'O 창ngulo de {angulo:.2f} tem a tangente {math.tan(math.radians(angulo)):.2f}.')